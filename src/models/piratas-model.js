class Pirata {
    constructor(id, nombre, apodo, fruta_del_diablo, tripulacion, edad, recompesa) {
        this.id = id;
        this.nombre = nombre;
        this.apodo = apodo;
        this.fruta_del_diablo = fruta_del_diablo;
        this.tripulacion = tripulacion;
        this.edad = edad;
        this.recompesa = recompesa;
    }
}

module.exports = Pirata;